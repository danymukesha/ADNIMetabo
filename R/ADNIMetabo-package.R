#' ADNIMetabo: Alzheimer’s Disease Neuroimaging Initiative (ADNI) Metabolomics and Related Datasets
#'
#' The \pkg{ADNIMetabo} package provides access to a curated collection of datasets from the Alzheimer’s Disease Neuroimaging Initiative (ADNI). This package is intended to facilitate research on Alzheimer's Disease by providing structured and easy-to-use demographic, clinical, imaging, biomarker, and cognitive data. Metabolomics data will be included in a future release.
#'
#' @section Datasets Included:
#' \describe{
#'   \item{Demographic data}{Includes participant information such as age, sex, education, ethnicity, and marital status.}
#'   \item{Baseline clinical data}{Baseline clinical assessments including diagnosis, CDR-SB, ADAS scores, MMSE, and RAVLT scores.}
#'   \item{Follow-up clinical data}{Longitudinal clinical assessments over follow-up visits.}
#'   \item{Biomarker data}{Includes CSF biomarker levels (ABETA, TAU, PTAU), FDG-PET, PIB, and AV45 imaging markers.}
#'   \item{Imaging data}{MRI-related measurements such as MRI field strength, FreeSurfer version, and various brain region volumes.}
#'   \item{Cognitive composite scores}{Composite scores like mPACCdigit and mPACCtrailsB for assessing cognitive performance.}
#'   \item{ECog scores}{Everyday Cognition (ECog) scores from both participants and their study partners.}
#'   \item{Metabolomic data}{To be made available in an upcoming release.}
#' }
#'
#' @section Acknowledgments:
#' The data included in this package is sourced from the Alzheimer’s Disease Neuroimaging Initiative (ADNI) database. ADNI is a multi-center study initiated in 2003 by the National Institute on Aging (NIA), National Institute of Biomedical Imaging and Bioengineering (NIBIB), Food and Drug Administration (FDA), private pharmaceutical companies, and nonprofit organizations.
